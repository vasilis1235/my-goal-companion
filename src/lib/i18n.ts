// BMI categories localized
export function bmiCategoryKey(bmi: number): string {
  if (bmi < 18.5) return "bmi.underweight";
  if (bmi < 25) return "bmi.normal";
  if (bmi < 30) return "bmi.overweight";
  if (bmi < 35) return "bmi.obese1";
  if (bmi < 40) return "bmi.obese2";
  return "bmi.obese3";
}

